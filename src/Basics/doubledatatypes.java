package Basics;
public class doubledatatypes {
    public static void main(String[] args) {
        double x=5;
        double y=2;
        double z=x/y;
        double w=5/2;
        double a=5.0/2;
        double b=5/2.0;
        double c=5.0/2.0;
        System.out.println(z);
        System.out.println(w);
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}

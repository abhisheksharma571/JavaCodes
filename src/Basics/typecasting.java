package Basics;

public class typecasting {
    public static void main(String[] args) {
        int a=45;
        double b;
        b=a;
        System.out.println(b);
        double num1=45.4;
        int num2;
        num2=(int)num1;
        System.out.println(num2);
        int n1=12;
        int n2=5;
        float res=n1/n2;
        System.out.println(res);
    }
}

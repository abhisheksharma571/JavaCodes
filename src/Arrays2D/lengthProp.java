package Arrays2D;

public class lengthProp {
    public static void main(String[] args) {
        int nums[]={5,4,3,6};
        System.out.println(nums.length);

        int nums2[][] = {{5, 2, 3, 7},
                        {4, 1},
                        {6, 5, 9}};
        System.out.println(nums2[0].length);

        String name="Abhishek";
        System.out.println(name.length());     //length method in string and length property in arrays
    }
}
